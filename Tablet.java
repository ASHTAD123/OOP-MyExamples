class Tablet {

	String nameOfTablet;
	String usage;
	int cost;

	public void setNameOfTablet(){
		this.nameOfTablet = nameOfTablet();
	}

	public String getNameOfTablet(){
		return nameOfTablet;
	}

	public void setUsageOfTablet(){
		this.usage = usage;
	}

	public String getUsage(){
		return usage;
	}

	public void setCost(){
		this.cost = cost;
	}

	public int getCost(){
		return cost;
	}
}